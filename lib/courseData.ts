// Вспомогательные функции
export const getAllLessons = (courseId?: string) => {
  const course = courseId ? coursesData.find(c => c.id === courseId) : courseData;
  if (!course) return [];
  return course.modules.flatMap(module => module.lessons);
};

export const getLessonById = (id: string, courseId?: string) => {
  return getAllLessons(courseId).find(lesson => lesson.id === id);
};

export const getNextLesson = (currentLessonId: string, courseId?: string) => {
  const allLessons = getAllLessons(courseId);
  const currentIndex = allLessons.findIndex(l => l.id === currentLessonId);
  return currentIndex >= 0 && currentIndex < allLessons.length - 1
    ? allLessons[currentIndex + 1]
    : null;
};

export const getPreviousLesson = (currentLessonId: string, courseId?: string) => {
  const allLessons = getAllLessons(courseId);
  const currentIndex = allLessons.findIndex(l => l.id === currentLessonId);
  return currentIndex > 0 ? allLessons[currentIndex - 1] : null;
};

export const getModuleProgress = (moduleId: string, completedLessons: string[], courseId?: string) => {
  const course = courseId ? coursesData.find(c => c.id === courseId) : courseData;
  if (!course) return 0;
  
  const module = course.modules.find(m => m.id === moduleId);
  if (!module) return 0;
  
  const moduleLessons = module.lessons;
  const completedCount = moduleLessons.filter(l => completedLessons.includes(l.id)).length;
  
  return Math.round((completedCount / moduleLessons.length) * 100);
};

// Функция для получения курса с тарифами из localStorage
export const getCourseWithPricing = (courseId: string): Course | undefined => {
  const course = coursesData.find(c => c.id === courseId);
  if (!course) return undefined;
  
  // Попытка загрузки тарифов из localStorage
  if (typeof window !== 'undefined') {
    try {
      const stored = localStorage.getItem(`nail_pricing_${courseId}`);
      if (stored) {
        const pricingPlans = JSON.parse(stored);
        return {
          ...course,
          pricingPlans
        };
      }
    } catch (error) {
      console.error('Ошибка загрузки тарифов из localStorage:', error);
    }
  }
  
  return course;
};

